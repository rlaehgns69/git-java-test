package section14;

public class Ex14_04_if {
    public static void main(String[] args) {
        int result = 0;
        int divisor = 0;
        if (divisor != 0) {
            result = 10 / divisor;
            System.out.println("나누기 결과" + result);

        } else {
            System.out.println("입력하신 내용을 계산할 수 없습니다.");
        }

    }

}
